let br = bracos[0];
let pe = peles[0];
let ca = cascos[0];
let ba = bases[0];

function statusSnail(braco, pele, casco, base) {
    const hp = document.getElementById('hp');
    const atk = document.getElementById('atk');
    const sAtk = document.getElementById('satk');
    const def = document.getElementById('def');
    const sDef = document.getElementById('sdef');
    const vel = document.getElementById('vel');

    hp.innerHTML = braco.hp + pele.hp + casco.hp + base.hp;
    atk.innerHTML = braco.atk + pele.atk + casco.atk + base.atk;
    sAtk.innerHTML = braco.sAtk + pele.sAtk + casco.sAtk + base.sAtk;
    def.innerHTML = braco.def + pele.def + casco.def + base.def;
    sDef.innerHTML = braco.sDef + pele.sDef + casco.sDef + base.sDef;
    vel.innerHTML = braco.vel + pele.vel + casco.vel + base.vel;
}

function changeBraco(value) {
    const braco = document.getElementById('braco');
    braco.style.backgroundColor = bracos[value].img;
    br = bracos[value];
    statusSnail(br, pe, ca, ba);
}

function changePele(value) {
    const pele = document.getElementById('pele');
    pele.style.borderBottom = `150px solid ${peles[value].img}`;
    pe = peles[value];
    statusSnail(br, pe, ca, ba);
}

function changeCasco(value) {
    const casco = document.getElementById('casco');
    casco.style.backgroundColor = cascos[value].img;
    ca = cascos[value];
    statusSnail(br, pe, ca, ba);
}

function changeBase(value) {
    const base = document.getElementById('base');
    base.style.backgroundColor = bases[value].img;
    ba = bases[value];
    statusSnail(br, pe, ca, ba);
}

function creatBtn(value, func) {
    const btn = document.createElement('button');
    btn.value = value.id;
    btn.className = 'btns';
    btn.innerText = value.id;
    btn.title = 
    `
HP: ${value.hp} 
ATK: ${value.atk} 
S.ATK: ${value.sAtk}
DEF: ${value.sDef} 
S.DEF: ${value.def} 
VEL: ${value.vel}
    `


    switch (func) {
        case 'changeBraco':
            btn.addEventListener('click', () => { changeBraco(value.id) });
            return btn;
        case 'changePele':
            btn.addEventListener('click', () => { changePele(value.id) });
            return btn;
        case 'changeCasco':
            btn.addEventListener('click', () => { changeCasco(value.id) });
            return btn;
        case 'changeBase':
            btn.addEventListener('click', () => { changeBase(value.id) });
            return btn;
    }

}

function loadOpts() {
    const bracosOpts = document.getElementById('bracos_opts');
    const pelesOpts = document.getElementById('peles_opts');
    const cascosOpts = document.getElementById('cascos_opts');
    const basesOpts = document.getElementById('bases_opts');


    for (const x of bracos) {
        bracosOpts.appendChild(creatBtn(x, 'changeBraco'));
    }

    for (const x of peles) {
        pelesOpts.appendChild(creatBtn(x, 'changePele'));
    }

    for (const x of cascos) {
        cascosOpts.appendChild(creatBtn(x, 'changeCasco'));
    }

    for (const x of bases) {
        basesOpts.appendChild(creatBtn(x, 'changeBase'));
    }
}

window.onload = () => {
    loadOpts();
    statusSnail(br, pe, ca, ba);
}